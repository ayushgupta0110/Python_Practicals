# Get an item of the tuple
tuplex = ("w", 3, "r", "e", "s", "o", "u", "r", "c", "e")
print(tuplex)

# Get item (4th element)of the tuple by index
# index starting from 0 i.e. 4th element is at index 3
item = tuplex[3]
print(item)

# Get item (4th element from last)by index negative
item1 = tuplex[-4]
print(item1)
